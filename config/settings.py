def _default_database_url():
    return "postgres://{user}:{password}@{host}:5432/{db}".format(
        user=env("POSTGRES_USER", default="messaging"),
        password=env("POSTGRES_PASSWORD", default="messaging"),
        host=env("POSTGRES_HOST", default="localhost"),
        db=env("POSTGRES_DB", default="messaging"),
    )


